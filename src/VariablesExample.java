public class VariablesExample {		// We will talk about this line at the end of the workshop
	public static void main(String[] args) {	// Eventually you will memorize this

		int x;		// Declaring a variable
		x = 1;		// Initializing variable
		System.out.println(x);		// Calling the println method, with the variable x as the parameter
		
		x = 2;
		System.out.println(x);
	}
}
